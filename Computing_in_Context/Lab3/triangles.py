# The triangle module has functions that perform
# calculations related to triangles.

# The perimeter function accepts a triangle's sides
# as arguments and returns the triangle's
# perimeter.
def perimeter(side1, side2, side3):
    return side1 + side2 + side3

# The area function accepts a triangle's base and
# height as arguments and returns the triangle's area.
def area(base, height):
    return (base * height) / 2


